// Příklady kódu s proměnnými a datovými typy

        // Celočíselné typy
        int age = 25;
        long bigNumber = 9223372036854775807;
        uint positiveNumber = 100;

        // Čísla s desetinnou čárkou
        float pi = 3.14f;
        double precisePi = 3.141592653589793;
        decimal money = 999.99m;

        // Symbol a řetězec
        char letter = 'A';
        string dropthealbum = "i am music ";

        // Logické typy
        bool isWorking = true;
        bool areYouSmart = false;

        // Nullable-typy
        int? nullableValue = null;

        // Výstupní hodnota
        Console.WriteLine($"Věk: {age}");
        Console.WriteLine($"Velké číslo: {bigNumber}");
        Console.WriteLine($"Kladné číslo: {positiveNumber}");
        Console.WriteLine($"Pi: {pi}");
        Console.WriteLine($"Přesné  Pi: {precisePi}");
        Console.WriteLine($"Peníze: {money}");
        Console.WriteLine($"Písmeno: {letter}");
        Console.WriteLine($"playboi carti: {dropthealbum}");
        Console.WriteLine($"Pracuje: {isWorking}");
        Console.WriteLine($"Jsi chytrý: {areYouSmart}");
        Console.WriteLine($"Nullable: {nullableValue}");
